export type Rarity = "normal" | "sssr";

// 演出の確率（テキストとは独立。どの文章でも一定確率で発動）
const TIER_WEIGHTS: Record<Rarity, number> = {
  normal: 99,
  sssr:    1,
};

export function rollRarity(): Rarity {
  const total = (Object.values(TIER_WEIGHTS) as number[]).reduce((a, b) => a + b, 0);
  let r = Math.random() * total;
  for (const [tier, w] of Object.entries(TIER_WEIGHTS) as [Rarity, number][]) {
    r -= w;
    if (r <= 0) return tier;
  }
  return "normal";
}

export function pickText(exclude?: string): string {
  let idx = Math.floor(Math.random() * MAGNETIC_TRIVIA.length);
  if (exclude && MAGNETIC_TRIVIA.length > 1) {
    let tries = 0;
    while (MAGNETIC_TRIVIA[idx] === exclude && tries++ < 10) {
      idx = Math.floor(Math.random() * MAGNETIC_TRIVIA.length);
    }
  }
  return MAGNETIC_TRIVIA[idx];
}

